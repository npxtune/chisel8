#pragma once

#include "raylib.h"

#define RAM_SIZE 4096
#define REGISTER_SIZE 0xF
#define STACK_SIZE 16
#define FONT_SIZE 80
#define DISPLAY_WIDTH 64
#define DISPLAY_HEIGHT 32
#define REFRESH_RATE 60
#define CLOCK_RATE 600

// FONT
static uint8_t FONT[FONT_SIZE] = {
        0xF0, 0x90, 0x90, 0x90, 0xF0, // 0
        0x20, 0x60, 0x20, 0x20, 0x70, // 1
        0xF0, 0x10, 0xF0, 0x80, 0xF0, // 2
        0xF0, 0x10, 0xF0, 0x10, 0xF0, // 3
        0x90, 0x90, 0xF0, 0x10, 0x10, // 4
        0xF0, 0x80, 0xF0, 0x10, 0xF0, // 5
        0xF0, 0x80, 0xF0, 0x90, 0xF0, // 6
        0xF0, 0x10, 0x20, 0x40, 0x40, // 7
        0xF0, 0x90, 0xF0, 0x90, 0xF0, // 8
        0xF0, 0x90, 0xF0, 0x10, 0xF0, // 9
        0xF0, 0x90, 0xF0, 0x90, 0x90, // A
        0xE0, 0x90, 0xE0, 0x90, 0xE0, // B
        0xF0, 0x80, 0x80, 0x80, 0xF0, // C
        0xE0, 0x90, 0x90, 0x90, 0xE0, // D
        0xF0, 0x80, 0xF0, 0x80, 0xF0, // E
        0xF0, 0x80, 0xF0, 0x80, 0x80  // F
};

typedef struct Chip8 {
    uint8_t delay, sound; // Delay and sound timers
    uint8_t ram[RAM_SIZE], reg[REGISTER_SIZE + 1];
    uint16_t stack[STACK_SIZE];
    uint16_t pc, I, opcode;

    int16_t key, i_stack;

    uint8_t pixels[DISPLAY_WIDTH][DISPLAY_HEIGHT];
    Texture2D display;
} emu;